def _get_selection_data(spreadsheet, as_text: bool = True) -> Tuple[Optional[str], List[List[str]], int, int, int, int]:
    """–ò–∑–≤–ª–µ–∫–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –∏–∑ –≤—ã–¥–µ–ª–µ–Ω–Ω–æ–≥–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ —Ç–∞–±–ª–∏—Ü—ã.
    
    Returns:
        (text_repr, data_matrix, start_row, start_col, end_row, end_col)
    """
    if not spreadsheet:
        return None, [], 0, 0, 0, 0
    
    selected = spreadsheet.selectedRanges()
    if not selected:
        return None, [], 0, 0, 0, 0
    
    r = selected[0]
    start_row = r.topRow()
    start_col = r.leftColumn()
    end_row = r.bottomRow()
    end_col = r.rightColumn()
    
    data = []
    lines = []
    
    # –ó–∞–≥–æ–ª–æ–≤–∫–∏ –∫–æ–ª–æ–Ω–æ–∫
    col_headers = []
    for c in range(start_col, end_col + 1):
        label = ""
        n = c
        while n >= 0:
            label = chr(65 + n % 26) + label
            n = n // 26 - 1
            if n < 0:
                break
        col_headers.append(label)
    
    if as_text:
        lines.append("Row | " + " | ".join(col_headers))
        lines.append("-" * (len(lines[0]) if lines else 40))
    
    for row in range(start_row, end_row + 1):
        row_data = []
        for col in range(start_col, end_col + 1):
            cell = spreadsheet.get_cell(row, col)
            val = ""
            if cell and cell.value is not None:
                val = str(cell.value)
            row_data.append(val)
        data.append(row_data)
        if as_text:
            lines.append(f"{row + 1} | " + " | ".join(row_data))
    
    text = "\n".join(lines) if as_text else None
    return text, data, start_row, start_col, end_row, end_col


